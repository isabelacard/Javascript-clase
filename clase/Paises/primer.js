//Ejercicio paises e idiomas

let pais = prompt("Ingresa el nombre del país: ");
let idioma = "";

if (pais === "Colomia") {
    idioma = "Español";
} else if (pais === "Italia") {
    idioma = "Italiano";
} else if (pais === "Alemania") {
    idioma = "Alemán";
} else if (pais === "China") { 
    idioma = "Mandarín";
} else if (pais === "Japón") {
    idioma = "Japonés";
} else if (pais === "Brasil") {
    idioma = "Portugués";
} else if (pais === "Rusia") {
    idioma = "Ruso";
} else if (pais === "India") {
    idioma = "Hindi";
}



